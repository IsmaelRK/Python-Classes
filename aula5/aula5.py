#  operadores aritiméticos

"""
+ adição
- subtração
* multiplicação
/ divisão
// divisão inteira (arredondado)
** exponenciação/potenciação
% resto da divisão/modulo, dá lhe o resto de uma divisão
() alterar as precedencias daa contas
"""

print('* multiplicação', 10 * 10)
print('+ adição', 10 + 10)
print('- subtração', 10 - 5)
print('/ divisão', 10 / 3)

print('* multiplicação/repetição', 10 * 'ismael')     #  10 * uma string

print('+ adição/concatenação', '10' + '10')
print('luiz' + ' ' + 'Otavio')
print('luiz', 'Otavio')
print('luiz' + ' ' + 'Otavio e ele tem ', str(32), 'anos')

print(10 // 3)
print(2 ** 10)
print(10 % 3)
print((5 + 2) * 10)
print('20'+'A')
print(20+2)
